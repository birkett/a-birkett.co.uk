namespace ABirkett\models;

/**
 * Super simple class to handle data for the postwidget controller.
 *
 * The method getAllPosts() returns only the essential post data, to reduce the
 * query result size.
 * Maybe rethink this, because this will still be alot of data once the blog
 * grows to a few hundred posts.
 *
 * @category  Models
 * @package   PersonalWebsite
 * @author    Anthony Birkett <anthony@a-birkett.co.uk>
 * @copyright 2015 Anthony Birkett
 * @license   http://opensource.org/licenses/MIT  The MIT License (MIT)
 * @link      http://www.a-birkett.co.uk
 */
class PostsWidgetModel extends BasePageModel
{


    /**
     * Get the post data and return it as an array
     * @return array Array of post data
     */
    public function getAllPosts()
    {
        return $this->database->runQuery(
            'SELECT postID, postTimestamp, postTitle FROM blog_posts '.
            'WHERE postDraft = "0" ORDER BY postTimestamp DESC',
            array()
        );

    }//end getAllPosts()
}//end class
