namespace ABirkett\models;

/**
 * Handles data for the Blog feed. Glue between the database and controller.
 *
 * The method getLatestPosts() will return all the needed data for the top X
 * posts, where X can be configured by BLOG_POSTS_PER_PAGE.
 *
 * @category  Models
 * @package   PersonalWebsite
 * @author    Anthony Birkett <anthony@a-birkett.co.uk>
 * @copyright 2015 Anthony Birkett
 * @license   http://opensource.org/licenses/MIT  The MIT License (MIT)
 * @link      http://www.a-birkett.co.uk
 */
class FeedPageModel extends BasePageModel
{


    /**
     * Get the posts data and return it as an array
     * @return array Array of posts data
     */
    public function getLatestPosts()
    {
        $rows = $this->database->runQuery(
            'SELECT postID, postTimestamp, postTitle, postContent'.
            ' FROM blog_posts WHERE postDraft = "0"'.
            ' ORDER BY postTimestamp DESC LIMIT 0,'.BLOG_POSTS_PER_PAGE,
            array()
        );

        return $rows;

    }//end getLatestPosts()
}//end class
